# Built-in Data Types

# In programming, understanding data types is fundamental.
# Variables can hold various types of data, each with its own set of capabilities.
# Python includes the following built-in data types across different categories:

# Text Type: str
# Numeric Types: int, float, complex
# Sequence Types: list, tuple, range
# Mapping Type: dict
# Set Types: set, frozenset
# Boolean Type: bool
# Binary Types: bytes, bytearray, memoryview
# None Type: NoneType
# These types enable Python to manage and manipulate diverse kinds of data efficiently.

# Getting the Data Type
